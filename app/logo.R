get_logo <- function() {

    logo <- "
                            /////////////////
                        ////      /////      ////
                      ///   //               //   ///
                    //  /                           //
                  //  // @     @@@@@@@@@@@@@@@@@   //  //
                /// //   @@@@@@@@@@@@@@@@@@@@@@@@@@@ // ///
               //  /     @@@@@@@@@@@@@@@@@@      @@@@  /  //
              //  /      @@@@@@@@@@@@@@@@@@         @   /  //
             //  /       @@    @@@@@@@@@@@@              /  //
             // /                @@@@@@@                  / //
            //  /               % %%                      /  //
            // /              %%  %  %%                    / //
            // /            %%  %   %                      / //
            // /          %%  %   %                        / //
            //  /       %   %   %                         /  //
             // /      %  %%  %         %   %             / //
             //  /    %  %%        %  %    %   %        /  //
             //  /  %% %%% %%  % %  %  % % %    %      /  //
              //  /  %   % %% %  % %  %%% %  %%      /  //
               /// // %%  % %  % %%%        %       // //
                 //  //  %%  %%%                  //  //
                   //   /                       /   //
                     ///   //               //   ///
                        ////      /////      ////
                            /////////////////
    "

    logo <- stringr::str_replace_all(logo, "@", crayon::blue("@"))
    logo <- stringr::str_replace_all(logo, "%", crayon::red("%"))
    logo <- stringr::str_replace_all(logo, "/", crayon::green("/"))

    banner <- "
                   ______ _   _   ___         _              _
                   | ___ \\ \\ | | / _ \\       | |            | |
           ___  ___| |_/ /  \\| |/ /_\\ \\______| |_ ___   ___ | |___
          / __|/ __|    /| . ` ||  _  |______| __/ _ \\ / _ \\| / __|
          \\__ \\ (__| |\\ \\| |\\  || | | |      | || (_) | (_) | \\__ \\
          |___/\\___\\_| \\_\\_| \\_/\\_| |_/       \\__\\___/ \\___/|_|___/

    "

    return(c(logo, banner))
}
